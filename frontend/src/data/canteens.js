// 展开为扁平的食堂名称列表（用于下拉选择）
export const canteenNames = campuses.flatMap(c =>
  c.canteens.map(ct => ct.name)
)

// 食堂名称 → 详细信息映射
export const canteenMap = {}
campuses.forEach(c => {
  c.canteens.forEach(ct => {
    canteenMap[ct.name] = { ...ct, campus: c.name }
  })
})

// 所有招牌菜（用于AI推荐等）
export const allFeaturedDishes = campuses.flatMap(c =>
  c.canteens.flatMap(ct => ct.dishes.slice(0, 4).map(d => ({ dish: d, canteen: ct.name, campus: c.name })))
)
